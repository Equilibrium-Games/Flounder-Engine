package flounder.platform;

/**
 * An enumeration of platform identifiers.
 */
public enum Platform {
	/**
	 * Windows 32-bit.
	 */
	WINDOWS_32,

	/**
	 * Windows 64-bit.
	 */
	WINDOWS_64,

	/**
	 * Linux 64-bit.
	 */
	LINUX_64,

	/**
	 * MacOS.
	 */
	MACOS,

	/**
	 * Webpage HTML5.
	 */
	HTML5,

	/**
	 * Android. 64 bit or 32 bit.
	 */
	ANDROID,

	/**
	 * Unknown platform.
	 */
	UNKNOWN
}
